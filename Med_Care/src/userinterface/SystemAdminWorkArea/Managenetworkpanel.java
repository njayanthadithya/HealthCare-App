    private void submitbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtActionPerformed
        logger.log(Level.INFO, "submitbtActionPerformed --Managenetworkpanel");
        vlb.setVisible(true);
       jPanel1.setVisible(true);
        if(citytxt.getText().isEmpty() || countrytxt.getText().isEmpty() || statetxt.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Please provide valid inputs", "warning",JOptionPane.WARNING_MESSAGE);
            return;
           
        }
        else{
        String city = citytxt.getText();
        String state =statetxt.getText();
        String country = countrytxt.getText();
                if(!system.getNetworkList().isEmpty())
        {
         for(Network network : system.getNetworkList())
         {
             if(city.equals(network.getName()))
             {
            JOptionPane.showMessageDialog(null, "Network already exists! ", "warning",JOptionPane.WARNING_MESSAGE);
            return;      
             }
         }
        }
                
        Network network = system.createNetwork();
        network.setName(city);
        network.setState(state);
        network.setCountry(country);

        getnetworktable();
        countrytxt.setText("");
        statetxt.setText("");
        citytxt.setText("");
        }
    }//GEN-LAST:event_submitbtActionPerformed

