    private void getnetworktable() {
        logger.log(Level.INFO, "getnetworktable --Managenetworkpanel");
        DefaultTableModel model = (DefaultTableModel) networktabe.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            Object[] row = new Object[3];
            row[0] = network.getName();
            row[1] = network.getState();
            row[2] = network.getCountry();
            model.addRow(row);
        }
    }

