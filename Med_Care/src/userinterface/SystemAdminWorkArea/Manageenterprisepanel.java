    private void deletebtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtActionPerformed
        // TODO add your handling code here:
        logger.log(Level.INFO, "deletebtActionPerformed --Manageenterprisepanel");
        int selectedrow = enterprisetable.getSelectedRow();
        if (selectedrow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the enterprise?");
            if (dialogResult == JOptionPane.YES_OPTION) {
                Enterprise enterprise = (Enterprise) enterprisetable.getValueAt(selectedrow, 0);
                for (Network network : system.getNetworkList()) {
                    this.enterprisedirectory = network.getEnterprisedirectory();
                }
                enterprisedirectory.deleteenterprise(enterprise);
                JOptionPane.showMessageDialog(null, "Deleted successfully");
                gettable();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }

    }//GEN-LAST:event_deletebtActionPerformed

