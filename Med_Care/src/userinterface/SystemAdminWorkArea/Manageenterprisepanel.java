    private void submitbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtActionPerformed
        logger.log(Level.INFO, "submitbtActionPerformed --Manageenterprisepanel");
        jPanel1.setVisible(true);
        deletebt.setVisible(true);
        vlb.setVisible(true);
        if (nametxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter valid name !");
            return;
        }

        Network net = (Network) networkJComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();

        if (net == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String name = nametxt.getText();

        for (Enterprise e : net.getEnterprisedirectory().getEnterpriselist()) {
            if (name.equalsIgnoreCase(e.getOrganzationName())) {
                JOptionPane.showMessageDialog(null, "Enterprise exits!");
                return;
            }
        }

        String mailID = mailtxt.getText();

        if (!mailID.isEmpty()) {
            Pattern VALID_EMAIL_ADDRESS_REGEX
                    = Pattern.compile("^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$", Pattern.CASE_INSENSITIVE);
            Matcher matcher = VALID_EMAIL_ADDRESS_REGEX.matcher(mailID);

            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(userProcessContainer, "Please enter email ID");
                return;
            }
        }

        Enterprise enterprise = net.getEnterprisedirectory().CreateEnterprise(name, type, mailID);

        for (Enterprise e
                : net.getEnterprisedirectory()
                        .getEnterpriselist()) {
            if (name.equalsIgnoreCase(e.getEmailid())) {
                JOptionPane.showMessageDialog(null, "MailID already exits!");
                return;
            }
        }

        JOptionPane.showMessageDialog(null, "Enterprise has been added successfully");
        gettable();

        nametxt.setText(
                "");
        mailtxt.setText(
                "");
        networkJComboBox.setSelectedIndex(
                0);
        enterpriseTypeJComboBox.setSelectedIndex(
                0);

    }//GEN-LAST:event_submitbtActionPerformed

