    private void gettable() {
        logger.log(Level.INFO, "gettable --Manageenterprisepanel");

        DefaultTableModel mod = (DefaultTableModel) enterprisetable.getModel();

        mod.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise ent : network.getEnterprisedirectory().getEnterpriselist()) {
                Object[] row = new Object[4];
                row[0] = ent;
                row[1] = network.getName();
                row[2] = ent.getEnterprisetype().getValue();
                try {
                    if (ent.getEmailid() == null || ent.getEmailid().isEmpty()) {
                        row[3] = "No MailID added";
                    } else {
                        row[3] = ent.getEmailid();
                    }
                } catch (NullPointerException e) {
                    row[3] = "No MailID added";
                }

                mod.addRow(row);
            }
        }
    }

