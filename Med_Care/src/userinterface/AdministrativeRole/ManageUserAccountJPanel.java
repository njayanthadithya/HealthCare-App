    public void getdata() {
        logger.log(Level.INFO, "getdata --ManageUserAccpanel");

        DefaultTableModel mod = (DefaultTableModel) userTable.getModel();

        mod.setRowCount(0);

        for (Organization organization : enterprise.getOrganizationdirectory().getOrganizationAList()) {
            for (UserAccount ua : organization.getUseraccountdirectory().getUseraccountlist()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) userTable.getModel()).addRow(row);
            }
        }
    }

