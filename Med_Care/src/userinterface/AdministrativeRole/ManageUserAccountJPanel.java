    private void submitbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtActionPerformed
        
         logger.log(Level.INFO, "submitbtActionPerformed --ManageUserAccpanel");
        
        jPanel1.setVisible(true);
        vlb.setVisible(true);
        
        try
        {
        String username = usernametxt.getText();
        String passWord = passwordtxt.getText();
        Organization organization = (Organization) orgcmbobox.getSelectedItem();
        if(username.isEmpty() || passWord.isEmpty())
            {
            JOptionPane.showMessageDialog(null, "Please provide inputs!", "warning", JOptionPane.WARNING_MESSAGE);  
            return;
            }
        
        for(UserAccount ua : organization.getUseraccountdirectory().getUseraccountlist()){
            if(username.equalsIgnoreCase(ua.getUsername())){
            JOptionPane.showMessageDialog(null, "User Name already exists!!", "warning", JOptionPane.WARNING_MESSAGE);  
            return;
            }
        }
        
        
        Employee employee = (Employee) empcombobox.getSelectedItem();
        Role role = (Role) rolecmbobox.getSelectedItem();

        UserAccount userAccount = organization.getUseraccountdirectory().createAccount(username, passWord, employee, role);
        userAccount.setNetwork((Network)useraccount.getNetwork());
        userAccount.setEnabled(true);
        getdata();
        JOptionPane.showMessageDialog(null, " User Account has been created successfully", "success",JOptionPane.PLAIN_MESSAGE);
        resetFields();
        }
        catch(Exception e)
       {
        JOptionPane.showMessageDialog(this, "Please enter valid data", "warning",JOptionPane.WARNING_MESSAGE);    
       }
    }//GEN-LAST:event_submitbtActionPerformed


