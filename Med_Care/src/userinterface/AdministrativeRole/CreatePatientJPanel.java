    private void savebtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtActionPerformed
        // TODO add your handling code here:  
        logger.log(Level.INFO, "savebtActionPerformed --createpatient");

        String patHRN = patienttxt.getText();

        if (patientnametxt.getText().length() == 0) {
            JOptionPane.showMessageDialog(userProcessContainer, "Enter Name");
            return;
        }
        if (patHRN.length() == 0) {
            JOptionPane.showMessageDialog(userProcessContainer, "Enter Patient ID");
            return;
        }
        for (Patient pat : enterprise.getPatientdirectory().getPatientAList()) {
            if (pat.getPatientHRN().equals(patHRN)) {
                JOptionPane.showMessageDialog(userProcessContainer, "Patient ID already exist");
                return;
            }
        }
        if (patientnametxt.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(userProcessContainer, "Only enter alphabets");
            return;
        }

        if (bloodcombobox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(userProcessContainer, "Choose a blood group");
            return;
        }
        Patient patient = enterprise.getPatientdirectory().addPatient();
        patient.setpName(patientnametxt.getText());
        patient.setPatientHRN(patHRN);
        patient.setBloodGroup(bloodcombobox.getSelectedItem().toString());
        JOptionPane.showMessageDialog(userProcessContainer, "Patient Sucessfully Created");      

        patienttxt.setText("");
        patientnametxt.setText("");
        bloodcombobox.setSelectedIndex(0);
    }//GEN-LAST:event_savebtActionPerformed

