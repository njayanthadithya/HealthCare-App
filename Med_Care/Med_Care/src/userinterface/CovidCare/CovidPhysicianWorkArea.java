    private void btnUpdateBloodUnitsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateBloodUnitsActionPerformed
        // TODO add your handling code here:
        logger.log(Level.INFO, "btnUpdateBloodUnitsActionPerformed --OBClinicianWorkArea");

        int selectedrow = OverallCovidTable.getSelectedRow();

        if (selectedrow >= 0) {
            
            CovidCareRequirementType ot = (CovidCareRequirementType) OverallCovidTable.getValueAt(selectedrow, 0);
            UpdateCovidtypeJPanel panel = new UpdateCovidtypeJPanel(userProcessContainer, ot, covidcarelist,enterprise,userAccount);
            userProcessContainer.add("CreateJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row"); }

    }//GEN-LAST:event_btnUpdateBloodUnitsActionPerformed

