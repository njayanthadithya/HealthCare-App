    void gettable() {
        logger.log(Level.INFO, "gettable --OBClinicianWorkArea");

        DefaultTableModel dtm = (DefaultTableModel) OverallCovidTable.getModel();
        dtm.setRowCount(0);

         //for (BloodGroup bloodgroup : bloodgrouplist.getBloodgrplist())
             
        for (CovidCareRequirementType ot : covidcarelist.getCCareTypeAList()) {

            Object row[] = new Object[2];
            row[0] = ot;
            row[1] = ot.getAvailUnits();
            //row[1] = bloodgroup.getAvailableunits();
            
            dtm.addRow(row);
        }
    }
    
    
    
    

